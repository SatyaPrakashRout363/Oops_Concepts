namespace Oops_Concepts
{
    using System;
    using System.Collections.Generic;
    using System.Linq;
    using System.Text;
    using System.Threading.Tasks;

    /// <summary>
    /// Eclass for Polymorphism.
    /// </summary>
    internal class Eclass
    {
        /// <summary>
        /// Initializes a new instance of the <see cref="Eclass"/> class.
        /// Constructor Overloading.
        /// </summary>
        public Eclass()
        {
        }

        /// <summary>
        /// Initializes a new instance of the <see cref="Eclass"/> class.
        /// overloading string value.
        /// </summary>
        /// <param name="value">value.</param>
        public Eclass(string value)
        {
        }

        /// <summary>
        /// Initializes a new instance of the <see cref="Eclass"/> class.
        /// overloading string value.
        /// </summary>
        /// <param name="value">value.</param>
        public Eclass(int value)
        {
        }

        /// <summary>
        /// Static method overload method string as parameter.
        /// </summary>
        /// <param name="value">value.</param>
        public static void Staticmethod(string value)
        {
        }

        /// <summary>
        /// Static method overload method char as parameter.
        /// </summary>
        /// <param name="value">value.</param>
        public static void Staticmethod(char value)
        {
        }

        /// <summary>
        /// Overloaded method as boolean parameter.
        /// </summary>
        /// <param name="value">value.</param>
        public void Overmethod(bool value)
        {
        }

        /// <summary>
        /// Overloaded method as string parameter.
        /// </summary>
        /// <param name="value">value.</param>
        public void Overmethod(string value)
        {
        }

        /// <summary>
        /// Overloaded method as int parameter.
        /// </summary>
        /// <param name="value">value.</param>
        public void Overmethod(int value)
        {
        }
    }
}
